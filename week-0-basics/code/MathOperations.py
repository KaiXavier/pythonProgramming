# 1. Add Two numbers

# Steps 1: Get input from the users
number_1 = int(input("Enter you first number => "))
number_2 = int(input("Enter you second number => "))

# Step 2: Add the two numbers and store in a variables
sum = number_1 + number_2

# Step 3: Print the output
print("Sum :", sum)

"""
Output:-
    Enter you first number => 3
    Enter you second number => 3
    Sum : 6

"""


